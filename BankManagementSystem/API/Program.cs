                options.UseSqlServer("Server=DESKTOP-MEGADM5\\MSSQLSERVER01;Database=bankManagmentSystem;Trusted_Connection=True;"));

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{

}

